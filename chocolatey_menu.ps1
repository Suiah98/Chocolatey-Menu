CheckForNewVersion

# Main loop
Do {
    ShowMenu
    $choice = Read-Host "Select apps by number you wish to install"
    
    # Check if the input is a number.
    if ($choice -match "^\d+$") {
        # If it's a valid number, toggle the selection
        if ([int]$choice -gt 0 -and [int]$choice -le $global:apps.Count) {
            $app = $global:apps[[int]$choice - 1]
            $app.Selected = If ($app.Selected -eq " ") { "x" } else { " " }
        }
        else {
            Write-Host "Invalid number. Please, select a number from the list." -ForegroundColor Red
            Read-Host "Press Enter to continue"
        }
    }
    # If not a number, check other options
    elseif ($choice -eq "E") {
        # Verification if no apps were selected
        $selectedApps = $global:apps | Where-Object { $_.Selected -eq "x" }
        if ($selectedApps.Count -eq 0) {
            Write-Host "Error! You haven't selected any app for installation." -ForegroundColor Red
            Read-Host "Press Enter to continue" # This will pause the script until the user presses Enter
        }
        Ensure-ChocolateyInstalled
        $selectedApps | ForEach-Object {
            choco install $_.Name -y
        }
    }
    elseif ($choice -eq "L") {
        ListInstalledChocolateyApps
    }
    elseif ($choice -eq "D") {
