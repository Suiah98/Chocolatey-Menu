# Function that checks if Chocolatey is installed and installs if necessary
Function Ensure-ChocolateyInstalled {
    if (-not (Get-Command choco -ErrorAction SilentlyContinue)) {
        Write-Host "------------------------------";
        Write-Host "Installing Chocolatey"; 
        Write-Host "------------------------------";
        Set-ExecutionPolicy Bypass -Scope Process -Force; 
        [System.Net.ServicePointManager]::SecurityProtocol = [System.Net.ServicePointManager]::SecurityProtocol -bor 3072; 
        iex ((New-Object System.Net.WebClient).DownloadString('https://community.chocolatey.org/install.ps1'))
    }
}

# Function that displays the main menu
Function ShowMenu {
    Clear-Host
    Write-Host "------------------------------"
    Write-Host "Chocolatey Menu by Suian -" $Menu_Version
    Write-Host "------------------------------"
    Write-Host "$(Get-ChocolateDessert)"
    Write-Host "------------------------------"
    Write-Host "Select the applications you want to install by marking with 'x'"
    Write-Host "                              "
    $index = 1
    $global:apps | ForEach-Object {
        Write-Host ("[" + $_.Selected + "] " + $index + ". " + $_.Name)
        $index++
    }
    Write-Host "------------------------------"
    Write-Host "                              "
    Write-Host "E. Execute selected installations"
    Write-Host "L. List applications installed with Chocolatey"
    Write-Host "D. Uninstall applications"
    Write-Host "U. Update all applications with Chocolatey"
    Write-Host "S. Exit"
    Write-Host "------------------------------"
    Write-Host "                              "
}

# Apps structure
$global:apps = @(
    @{Name = "googlechrome"; Selected = "x" },
    @{Name = "vscode"; Selected = " " }
)

# Function that displays all installed apps
Function ListInstalledChocolateyApps {
    Clear-Host
    Write-Host "Applications installed with Chocolatey:"
    choco list
    Read-Host "Press Enter to return to the menu"
}

# Function that updates all apps
Function UpdateAllChocolateyApps {
    Clear-Host
    Write-Host "Updating all applications with Chocolatey..."
    choco upgrade all -y
    Write-Host "Update completed."
    Read-Host "Press Enter to return to the menu"
}

# Function to update the script if there's a new version
Function UpdateToLatestVersion {
    $scriptUrl = "https://raw.githubusercontent.com/Suiah98/Chocolatey-Menu/main/Chocolatey_Menu.ps1"
    $localPath = $PSScriptRoot + "\Chocolatey_Menu.ps1"

    # Download the new script
    Invoke-WebRequest -Uri $scriptUrl -OutFile $localPath

    Write-Host "The script has been updated to the latest version." -ForegroundColor Green
    Write-Host "Restarting the script..." -ForegroundColor Green
    Start-Sleep -Seconds 3

    # Restart the script
    & $localPath
    exit
}

# Function to check the latest script version
Function CheckForNewVersion {
    $versionUrl = "https://raw.githubusercontent.com/Suiah98/Chocolatey-Menu/main/menu_version.txt"
    $latestVersion = (Invoke-WebRequest -Uri $versionUrl).Content.Trim() # Use Trim() to clean possible spaces or line breaks

    if ($latestVersion -gt $Menu_Version) {
        Write-Host "Attention! There's a new version of the script available: $latestVersion" -ForegroundColor Yellow
        $userResponse = Read-Host "Do you want to update now? (Y/N)"

        if ($userResponse -eq 'Y' -or $userResponse -eq 'y') {
            UpdateToLatestVersion
        }
    }
}

# Function that displays the installed applications and allows uninstalling
Function UninstallChocolateyApps {
    Clear-Host
    Write-Host "Select the application you want to uninstall:"
    $installedApps = choco list --local-only
    $index = 1
    $appList = @()
    foreach ($app in $installedApps) {
        if ($app -notmatch "^(\d+ packages installed\.|chocolatey|cyg-get|cygwin|windowsfeatures)") {
            $appName = $app -replace " .*"
            Write-Host ("[" + $index + "] " + $appName)
            $appList += $appName
            $index++
        }
    }
    $uninstallChoice = Read-Host "Select by number the app you want to uninstall or 'V' to return to the menu"
    if ($uninstallChoice -eq "V") {
        return
    }
    if ($uninstallChoice -match "^\d+$" -and [int]$uninstallChoice -gt 0 -and [int]$uninstallChoice -le $appList.Length) {
        $selectedApp = $appList[[int]$uninstallChoice - 1]
        choco uninstall $selectedApp -y
        Read-Host "Application uninstalled. Press Enter to continue"
    }
    else {
        Write-Host "Invalid option." -ForegroundColor Red
        Read-Host "Press Enter to continue"
    }
}

